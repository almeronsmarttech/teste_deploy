def buscar_parametros_laje(self, razao, tabela):
    linha_mais_proxima = min(tabela, key=lambda linha: abs(linha[0] - round(razao, 2)))
    # Nomes padronizados dos campos
    colunas = [
        "relacao", "wc", "mxe", "mye", "mx", "my", "mxy",
        "rxe", "rx", "rye", "ry", "beta_x", "beta_y"
    ]
    # Cria o dicionário associando cada nome de campo ao valor da tupla
    return dict(zip(colunas, linha_mais_proxima))


